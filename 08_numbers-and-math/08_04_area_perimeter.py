# Write code to display the area and perimeter
# of a rectangle that has a width of 2.4 and a height of 6.4.

width = 2.4
height = 6.4

area = float(width * height)
perimeter = float((width * 2) + (height * 2))

print("The total Area is " + str(area))
print("The total perimeter is " + str(perimeter))
